$( window ).resize(function() {
	if ($( window ).width() < '720') {
		$("input").css({
			'fontSize': '14px',
		});
	} else {
		$("input").css({
			'fontSize': '14px',
		});
	}
	if ($( window ).width() < '720') {
		$("h1").css({
			'fontSize': '12px',
		});
	} else {
		$("h1").css({
			'fontSize': '14px',
		});
	}
});	